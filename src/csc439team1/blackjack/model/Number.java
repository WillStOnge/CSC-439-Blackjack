package csc439team1.blackjack.model;

/**
 * The Number enum represents the 13 types of playing card 'numbers'.
 * Note the ordering of the card numbers:
 * 0(ACE), 1(TWO), 2(THREE), ... , 12(KING)
 *
 * @author Justin Gallagher
 * @version 0.1
 */
public enum Number
{
	ACE,
	TWO,
	THREE,
	FOUR,
	FIVE,
	SIX,
	SEVEN,
	EIGHT,
	NINE,
	TEN,
	JACK,
	QUEEN,
	KING
}
